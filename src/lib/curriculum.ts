export type ExerciseType = "mcq" | "truefalse" | "order";
export type LessonKind =
  | "video"
  | "exercise"
  | "section-quiz"
  | "unit-exam"
  | "level-exam";

export interface Exercise {
  id: string;
  type: ExerciseType;
  question: string;
  options?: string[];
  answer: number | number[];
  explanation?: string;
}

export interface Lesson {
  id: string;
  unitId: string;
  title: string;
  summary: string;
  minutes: number;
  xp: number;
  keyPoints: string[];
  videoEmbed?: string;
  exercises: Exercise[];
  /** Type of node in the learning path. Defaults to "exercise" for legacy data. */
  kind?: LessonKind;
}

export interface Unit {
  id: string;
  levelId: string;
  title: string;
  description: string;
  icon: string;
  lessons: Lesson[];
}

export interface Level {
  id: string;
  number: number;
  title: string;
  description: string;
  units: Unit[];
}

// ----- helpers to keep content authoring concise -----
const mcq = (
  id: string,
  question: string,
  options: string[],
  answer: number,
  explanation?: string,
): Exercise => ({ id, type: "mcq", question, options, answer, explanation });

const tf = (
  id: string,
  question: string,
  answer: 0 | 1,
  explanation?: string,
): Exercise => ({
  id,
  type: "truefalse",
  question,
  options: ["صحيح", "خطأ"],
  answer,
  explanation,
});

interface BaseLesson {
  id: string;
  title: string;
  summary: string;
  minutes?: number;
  xp?: number;
  kind: LessonKind;
  keyPoints?: string[];
  videoEmbed?: string;
  exercises?: Exercise[];
}

const makeLesson = (unitId: string, l: BaseLesson): Lesson => ({
  id: l.id,
  unitId,
  title: l.title,
  summary: l.summary,
  minutes:
    l.minutes ??
    (l.kind === "video"
      ? 5
      : l.kind === "exercise"
      ? 4
      : l.kind === "section-quiz"
      ? 6
      : l.kind === "unit-exam"
      ? 10
      : 15),
  xp:
    l.xp ??
    (l.kind === "video"
      ? 15
      : l.kind === "exercise"
      ? 20
      : l.kind === "section-quiz"
      ? 35
      : l.kind === "unit-exam"
      ? 60
      : 100),
  keyPoints: l.keyPoints ?? [],
  videoEmbed: l.videoEmbed,
  exercises: l.exercises ?? [],
  kind: l.kind,
});

