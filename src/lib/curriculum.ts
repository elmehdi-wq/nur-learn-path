// Generic builder for the remaining levels — keeps content concise but real
// =====================================================================
interface UnitSpec {
  id: string;
  title: string;
  description: string;
  icon: string;
  videoTopic: string;
  videoKeyPoints: string[];
  exercises: { title: string; questions: Exercise[] }[];
  sectionQuiz: Exercise[];
  unitExam: Exercise[];
}

interface LevelSpec {
  id: string;
  number: number;
  title: string;
  description: string;
  units: UnitSpec[];
  levelExam: Exercise[];
}

function buildLevel(spec: LevelSpec): Level {
  return {
    id: spec.id,
    number: spec.number,
    title: spec.title,
    description: spec.description,
    units: spec.units.map((u) => {
      const lessons: Lesson[] = [];
      lessons.push(
        makeLesson(u.id, {
          id: `${u.id}-v1`,
          kind: "video",
          title: `فيديو: ${u.videoTopic}`,
          summary: `شرح مرئي مبسّط حول ${u.videoTopic}.`,
          keyPoints: u.videoKeyPoints,
          exercises: [tf("v1", "شاهدت الفيديو بانتباه.", 0)],
        }),
      );
      u.exercises.forEach((ex, i) =>
        lessons.push(
          makeLesson(u.id, {
            id: `${u.id}-e${i + 1}`,
            kind: "exercise",
            title: `تمرين ${i + 1}: ${ex.title}`,
            summary: `أسئلة تطبيقية على ${ex.title}.`,
            exercises: ex.questions,
          }),
        ),
      );
      lessons.push(
        makeLesson(u.id, {
          id: `${u.id}-sq`,
          kind: "section-quiz",
          title: `اختبار القسم — ${u.title}`,
          summary: "تثبيت ما سبق من تمارين.",
          exercises: u.sectionQuiz,
        }),
      );
      lessons.push(
        makeLesson(u.id, {
          id: `${u.id}-ue`,
          kind: "unit-exam",
          title: `اختبار الوحدة: ${u.title}`,
          summary: "اختبار شامل لكل دروس الوحدة.",
          exercises: u.unitExam,
        }),
      );
      return {
        id: u.id,
        levelId: spec.id,
        title: u.title,
        description: u.description,
        icon: u.icon,
        lessons,
      } as Unit;
    }),
  };
}

function addLevelExam(level: Level, questions: Exercise[]): Level {
  const lastUnit = level.units[level.units.length - 1];
  const examLesson = makeLesson(lastUnit.id, {
    id: `${level.id}-final`,
    kind: "level-exam",
    title: `اختبار المستوى ${level.number}`,
    summary: `اختبار نهائي لكل ما تعلّمته في المستوى ${level.number}.`,
    exercises: questions,
  });
  lastUnit.lessons.push(examLesson);
  return level;
}

